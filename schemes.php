            <h2>Major Programmes of the Department</h2>
            <h3>C. Additional Central  Assistance</h3>
            <h3>1. Watershed Development Project in  Shifting Cultivation Areas (WDPSCA)</h3>
            <p>The  scheme was implemented from the year 1995-96 onwards. It is a 100% Central  assistance through the Ministry of Agriculture &amp; Co-operation, Government  of India.<br />
              The  main thrust of the project is as follows:</p>
            <ul>
              <li>Protect hill slopes of jhum areas  through soil and water conservation measures on a watershed basis.</li>
              <li>Encourage and assist jhummia families to  develop jhum land for productive uses with improved cultivation and suitable  package of practices leading to settled cultivation.</li>
              <li>Improved socio-economic status of  jhummia families through household/land-based activities.</li>
              <li>Mitigate ill effects of shifting  cultivation by introducing appropriate land use as per land capacity and  improved technologies.</li>
            </ul>
            <p>During  2012-13, Government of India did not release any fund for the scheme.</p>
            <h3>2. Accelerated Irrigation Benefits  Programme (AIBP)</h3>
            <p>This  is a Central assistance scheme sponsored by the Ministry of Water Resources. It  is in the form of central grant (90 % of the project cost) and the balance cost  of the project (10%) as the State’s share which is to be arranged by the state  government from its own resources. The main thrust of the programme is to  increase the area under irrigation thereby increasing the productivity of the  area and improving the socio-economic condition of the people.</p>
            <p>During  2011-12, a total of 32 projects sanctioned during 2009-10 have been completed  and no new projects were sanctioned by the Government of India during the year  2012-13.</p>
            <h3>D. NABARD Loan- Soil  &amp; Water Conservation Scheme under RIDF</h3>
            <p>The  Department started implementing Scheme under Rural Infrastructure Development  Fund (RIDF) - NABARD Loan from the year 2000-2001 onwards. The basic objective  of the scheme is to enhance the productivity of agriculture and its allied  activities and in small river valleys, thereby improving the socio-economic set  up of the people in the rural areas. Besides these, the scheme also envisages  to promote sustainable development through conservation and management of soil  and water.</p>
            <p>A total  of 49 projects had been implemented under the RIDF NABARD Loan. Out of these,  12 projects are under RIDF-V (2000-01 to 2002-03), 11 projects under RIDF-IX  (2004-05 to 2006-07), 7 projects under RIDF· XIII (2007-08 to 2009-10), 10  projects under RIDF XIV (2009-10 onwards) and 9 projects under RIDF-XV. 40 Projects  under RIDF-V, RIDF-IX and RIDF· XIII and RIDF XIV have been completed, while 9  projects under RIDF-XV are ongoing.</p>
            <h3>E. Other Government of  India Schemes</h3>
            <h3>1. Soil and Water Conservation in the  Catchment of River Kopili</h3>
            <p>The  Department is implementing a scheme known as River Valley Project &amp; Flood  Prone River (RVP &amp; FPR) of River Kopili under the Macro-management mode.  The State Agriculture Department is the Nodal Department of this centrally  sponsored programme of the Ministry Of Agriculture, Department of Agriculture  &amp; Co-operation (Natural Resource Management Division).</p>
            <p><strong>Objectives:-</strong></p>
            <ul>
              <li>Prevention of land degradation by  adoption of appropriate based soil and water conservation measures on watershed  approach.</li>
              <li>Improvement of land capability and  moisture regime in the watersheds.</li>
              <li>Promotion of land-use to match land  capability.</li>
              <li>Prevention of soil erosion and run off  from the watershed to prevent premature siltation of reservoirs.</li>
            </ul>
            <p>The selection of the watersheds was  based as per the priority list of the watersheds. The Soil and Land Use Survey  of India (SLUSI) was engaged in the task of priority delineation and detailed  Soil survey of the Kopili Catchment. The watersheds have been prioritized based  on the magnitude and criticality of degradation into five categories; viz, Very  High, High, Medium, Low and Very Low.</p>
