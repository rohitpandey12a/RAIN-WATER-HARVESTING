            <h3>2. Rashtriya  Krishi Vigyan Yojana (RKVY)</h3>
            <p>The  area of focus of this programme is for the development of Rainfed Farming  Systems in and outside watershed areas as also integrated development of  watershed areas, wastelands, river valleys and for activities relating to  enhancement of crop production and popularization of micro-irrigation systems.  The thrust area is to protect the loss of topsoil, improving soil fertility,  enhancing crop production, land and water productivity of watershed areas  comprising of wastelands, river valleys and the eco-system as a whole. The  programme is implemented with the Department of Agriculture as the Nodal  Agency.</p>
            <p><strong>Objectives </strong></p>
            <ul>
              <li>To incentivize  the States so as to increase public investment in Agriculture &amp; Allied  Sectors. </li>
              <li>To provide  flexibility &amp; autonomy to States in the process of planning and executing  Agriculture &amp; Allied Sector Schemes </li>
              <li>To ensure the  preparation of agriculture plans for the Districts and the States based on  agro-c1imatic conditions, availability of Technology and natural resources. </li>
              <li>To ensure that  the local needs/ crops/ priorities are reflected in the agricultural plans of  the states. </li>
              <li>To achieve the  goal of reducing the yield gaps in important crops, through focused  interventions. </li>
              <li>To maximize  returns to the farmers in Agriculture &amp; Allied Sectors. </li>
              <li>To bring about  quantifiable changes in the production &amp; productivity of various components  of Agriculture &amp; Allied Sectors by addressing them in a holistic manner.</li>
            </ul>
            <p>The proposed schemes  under RKVY mainly comprise of the following: </p>
            <ol start="1" type="a">
              <li>Soil &amp; Water Conservation for enhancing crop production &amp;       productivity in river valley/ valley bottom lands. </li>
              <li>Soil &amp; Water Conservation for improving crop production &amp;       productivity of cultivated Jhum &amp; Bun lands including abandoned Jhum       &amp; Bun lands. </li>
              <li>Soil &amp; Water Conservation for restoring &amp; reclaiming       cultivable wastelands affected by mining &amp; quarrying </li>
              <li>Soil &amp; Water Conservation for improvement of traditional water       conservation &amp; distribution system for enhanced crop production. </li>
            </ol>
            <h3>F.  Cherrapunjee Ecological Project- Restoration of Degraded Lands under Sohra  Plateau</h3>
            <p>This project under Special Plan  Assistance (SPA) was sanctioned by the Govt. Of India during 2010-11. The  objective of the Scheme is to enhance Soil moisture/water regime by the  following ways.</p>
            <ul>
              <li>Ameliorate the ecology and environment  of Sohra Plateau.</li>
              <li>Restore degraded lands</li>
              <li>Strengthen village level institutions or  NRM</li>
              <li>Minimize human activities detrimental to  the environment</li>
              <li>Create avenues for sustainable  livelihood</li>
            </ul>
            <p>The main objectives of the project are as  follows:-</p>
            <ul>
              <li>Check soil loss and reduce silt load in  streams and rivers</li>
              <li>Improve soil moisture regime</li>
              <li>Promote in-<em>situ</em> water harvesting </li>
              <li>Improve recharge of springs and aquifers </li>
              <li>Afforestation/ reforestation of denuded  hills/areas</li>
              <li>Promote development of pasture lands and  horticulture plantations</li>
              <li>Imparting training and capacity building  for the villagers/ farmers on sustainable farming practices </li>
              <li>Provide support for livelihood  activities-piggery, poultry, fisheries </li>
              <li>Create awareness amongst the people on  natural resource management </li>
            </ul>
